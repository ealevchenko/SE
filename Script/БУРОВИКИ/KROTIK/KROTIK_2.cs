        public void Main(string argument, UpdateType updateSource)
        {
            StringBuilder values_info = new StringBuilder();
            bats.Logic(argument, updateSource);

            switch (argument)
            {
                case "connected_on":
                    connector.Connect();
                    ConnectedOn();
                    break;
                case "connected_off":
                    connector.Disconnect();
                    ConnectedOff();
                    break;
                case "connected":
                    if (ship_connect)
                    {
                        connector.Disconnect();
                        ConnectedOff();
                    }
                    else
                    {
                        connector.Connect();
                        ConnectedOn();
                    }
                    break;
                case "horizont_on":
                    gyros.GyroOver(true);
                    horizont = true;
                    break;
                case "horizont_off":
                    gyros.GyroOver(false);
                    horizont = false;
                    break;
                case "horizont":
                    if (horizont)
                    {
                        gyros.GyroOver(false);
                        horizont = false;
                    }
                    else
                    {
                        gyros.GyroOver(true);
                        horizont = true;
                    }
                    break;
                default:
                    break;
            }
            if (updateSource == UpdateType.Update10)
            {
                if (!ship_connect && connector.Connected)
                {
                    // Включили парковку
                    ConnectedOn();
                }
                if (ship_connect && !connector.Connected)
                {
                    // Выключли парковку
                    ConnectedOff();
                }
                // Проверка кокпит под контроллем
                if (!connector.Connected && !cockpit.IsUnderControl)
                {
                    cockpit.Dampeners(true);
                    reflectors_light.Off();
                    drill.Off();
                }
                // Держать горизонт
                if (!connector.Connected && horizont)
                {
                    KeepHorizon();
                }

            }
            values_info.Append(bats.TextInfo());
            values_info.Append(connector.TextInfo());
            values_info.Append("РЕЖИМ:" + (horizont ? "ГОРИЗОНТ" : "") + "\n");
            cockpit.OutText(values_info, 0);
            ship_connect = connector.Connected; // сохраним состояние
        }
