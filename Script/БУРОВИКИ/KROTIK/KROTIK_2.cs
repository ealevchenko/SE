        public class Batterys : BaseListTerminalBlock<IMyBatteryBlock>
        {
            public int count_work_batterys { get { return list_obj.Where(n => !((IMyTerminalBlock)n).CustomName.Contains(tag_batterys_duty)).Count(); } }
            public bool charger = false;
            public Batterys(string name_obj) : base(name_obj)
            {
                Init();
            }
            public Batterys(string name_obj, string tag) : base(name_obj, tag)
            {
                Init();
            }
            public void Init()
            {
                base.On();
                charger = IsCharger();
            }
            public float MaxPower()
            {
                return base.list_obj.Select(b => b.MaxStoredPower).Sum();
            }
            public float CurrentPower()
            {
                return base.list_obj.Select(b => b.CurrentStoredPower).Sum();
            }
            public int CountCharger()
            {
                List<IMyBatteryBlock> res = base.list_obj.Where(b => ((IMyBatteryBlock)b).ChargeMode == ChargeMode.Recharge).ToList();
                return res.Count();
            }
            public int CountAuto()
            {
                List<IMyBatteryBlock> res = base.list_obj.Where(b => ((IMyBatteryBlock)b).ChargeMode == ChargeMode.Recharge).ToList();
                return res.Count();
            }
            public bool IsCharger()
            {
                int count_charger = CountCharger();
                return count_work_batterys > 0 && count_charger > 0 && count_work_batterys == count_charger ? true : false;
            }
            public bool IsAuto()
            {
                int count_auto = CountAuto();
                return Count > 0 && count_auto > 0 && Count == count_auto ? true : false;
            }
            public void Charger()
            {
                foreach (IMyBatteryBlock obj in base.list_obj)
                {
                    // проверка батарея дежурного режима
                    if (!obj.CustomName.Contains(tag_batterys_duty))
                    {
                        obj.ChargeMode = ChargeMode.Recharge;
                    }
                }
                charger = IsCharger();
            }
            public void Auto()
            {
                foreach (IMyBatteryBlock obj in base.list_obj)
                {
                    obj.ChargeMode = ChargeMode.Auto;
                }
                charger = IsCharger();
            }
            public void Logic(string argument, UpdateType updateSource)
            {
                switch (argument)
                {
                    case "bat_charger":
                        Charger();
                        break;
                    case "bat_auto":
                        Auto();
                        break;
                    case "bat_toggle":
                        if (charger) { Auto(); } else { Charger(); }
                        break;
                    default:
                        break;
                }

                if (updateSource == UpdateType.Update10)
                {

                }

            }

            public string TextInfo()
            {
                StringBuilder values = new StringBuilder();
                //БАТАРЕЯ: [10 - 10] [0.0MW / 0.0MW]
                //|- ЗАР:  [''''''''''''''''''''''''']-0%
                //| -ДЕЖ: -[''''''''''''''''''''''''']-0%
                values.Append("БАТАРЕЯ: [" + Count + "] [А-" + CountAuto() + " З-" + CountCharger() + "]" + PText.GetCurrentOfMax(CurrentPower(), MaxPower(), "кW") + "\n");
                values.Append("|- ЗАР:  " + PText.GetScalePersent(CurrentPower() / MaxPower(), 20) + "\n");
                return values.ToString();
            }
        }
        public class BatteryBlock
        {
            List<IMyBatteryBlock> batterylist = new List<IMyBatteryBlock>();
            public int Count { get { return batterylist.Count(); } }
            public BatteryBlock(string name_group)
            {

                _scr.GridTerminalSystem.GetBlocksOfType<IMyBatteryBlock>(batterylist, r => r.CustomName.Contains(name_group));
                _scr.Echo("batterylist: " + ((batterylist != null && batterylist.Count() > 0) ? ("Ок") : ("not batterylist")));
            }

            public string GetMode()
            {
                string result = "";
                foreach (IMyBatteryBlock batt in batterylist)
                {
                    result = batt.ChargeMode.ToString();
                }
                return result;
            }

            public string GetModeOfText()
            {
                string result = "БАТ:";
                foreach (IMyBatteryBlock batt in batterylist)
                {
                    switch (batt.ChargeMode)
                    {

                        case ChargeMode.Auto:
                            {
                                result += "А|";
                                break;
                            };
                        case ChargeMode.Recharge:
                            {
                                result += "З|";
                                break;
                            };
                        case ChargeMode.Discharge:
                            {
                                result += "Р|";
                                break;
                            };
                    }
                }
                return result;
            }
            public void Charger()
            {
                foreach (IMyBatteryBlock batt in batterylist)
                {
                    batt.ChargeMode = ChargeMode.Recharge;
                }
            }
            public void Auto()
            {
                foreach (IMyBatteryBlock batt in batterylist)
                {
                    batt.ChargeMode = ChargeMode.Auto;
                }
            }
        }
